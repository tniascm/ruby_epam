#1. Є масив з довільними числами.
# Зробіть так, щоб елемент повторився в
# масиві таку кількість разів яка відповідає його числу.
# Приклад: [1, 3, 2, 4] перетворюється в
# [1, 3, 3, 3, 2, 2, 4, 4, 4, 4].

p a = [1, 3, 2, 4]

def arr(array)
  final = Array.new
  array.each { |i|
    (0..i - 1).each { final << i }
  }
  p final
end

arr(a)

